import sys
def reverse1(x):
    num = abs(x)
    rev = 0
    
    while num != 0:
        ld = num % 10
        
        # Overflow check
        if rev > (sys.maxsize - ld) / 10:
            return 0
        
        rev = rev * 10 + ld
        num = num // 10
    
    if x < 1:
        rev = rev * (-1)
    
    return rev


